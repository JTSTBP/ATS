// Helper function to search cities
export const searchCities = (query) => {
  if (!query || query.length < 2) return [];

  const searchTerm = query.toLowerCase();
  return allIndianCities
    .filter((city) => city.name.toLowerCase().includes(searchTerm))
    .sort((a, b) => {
      // Prioritize exact matches and tier 1 cities
      const aExact = a.name.toLowerCase().startsWith(searchTerm);
      const bExact = b.name.toLowerCase().startsWith(searchTerm);

      if (aExact && !bExact) return -1;
      if (!aExact && bExact) return 1;

      // Then sort by tier
      const tierOrder = { "Tier 1": 1, "Tier 2": 2, "Tier 3": 3 };
      return tierOrder[a.tier] - tierOrder[b.tier];
    })
    .slice(0, 10); // Limit to 10 results
};

