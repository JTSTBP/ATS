// Helper function to search industries
export const searchIndustries = (query) => {
  if (!query || query.length < 1) return [];

  const searchTerm = query.toLowerCase();
  return industryData
    .filter((industry) => industry.toLowerCase().includes(searchTerm))
    .sort((a, b) => {
      // Prioritize exact matches and those starting with the search term
      const aExact = a.toLowerCase().startsWith(searchTerm);
      const bExact = b.toLowerCase().startsWith(searchTerm);

      if (aExact && !bExact) return -1;
      if (!aExact && bExact) return 1;

      return a.localeCompare(b);
    })
    .slice(0, 10); // Limit to 10 results
};

// Helper function to get streams for a degree
export const getStreamsForDegree = (degreeValue, educationType) => {
  let data;
  switch (educationType) {
    case "graduation":
      data = graduationData;
      break;
    case "postGraduation":
      data = postGraduationData;
      break;
    case "doctoral":
      data = doctoralData;
      break;
    default:
      return [];
  }

  const degree = data.degrees.find((d) => d.value === degreeValue);
  return degree ? degree.streams : [];
};
